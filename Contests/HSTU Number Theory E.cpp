intl zero(intl n){
    intl res = 0;
    while(n/5){
        res += n/5;
        n /= 5;
    }
    return res;
}


int main()
{
    int tc; sfi(tc);

    intl q;
    for(int t=1; t<=tc; t++){
        sfl(q);

        intl l=1 , r = 1e18, m, ans = -1, v;
        while(l<=r){
            m = (l+r) / 2;
            v = zero(m);
            if(v>q) r = m-1;
            else if(v==q) ans = m, r = m-1;
            else l = m+1;
        }

        if(ans != -1) printf("Case %d: %lld\n", t, ans);
        else printf("Case %d: impossible\n", t);
    }


    return 0;
}

